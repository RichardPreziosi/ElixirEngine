namespace ElixirEngine
{
    /// <summary>
    ///     Indicates the current orientation of a window.
    /// </summary>
    public enum WindowOrientation
    {
        /// <summary>
        ///     The window is wider than it is tall.
        /// </summary>
        Landscape,

        /// <summary>
        ///     The window is taller than it is wide.
        /// </summary>
        Portrait
    }
}